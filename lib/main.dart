Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();

  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  await GetStorage.init();
  Get.put(ApiService());
  Get.put(NotificationController());

  // Firebase Messaging
  try {
    FirebaseMessaging messaging = FirebaseMessaging.instance;

    NotificationSettings settings = await messaging.requestPermission(
      alert: true,
      badge: true,
      sound: true,
    );
    print('ğŸ“Œ Notification Permission: ${settings.authorizationStatus}');

    // ğŸ“± Device Token
    String? deviceToken = await messaging.getToken();
    print('ğŸ“± Device Token: $deviceToken');

    // ğŸ”‘ Firebase Access Token
    String? accessToken = await getAccessToken();
    print('ğŸ”‘ Firebase Access Token: $accessToken');

  } catch (e) {
    print("âš ï¸ Notification permission error: $e");
  }

  FirebaseMessaging.onBackgroundMessage(_firebaseMessagingBackgroundHandler);

  runApp(const MyApp());
}

